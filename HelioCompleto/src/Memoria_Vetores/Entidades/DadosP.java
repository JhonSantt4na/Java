package Memoria_Vetores.Entidades;

public class DadosP {
  double altura;
  char genero;

  public DadosP(double altura, char genero) {
    this.altura = altura;
    this.genero = genero;
  }

  public double getAltura() {
    return altura;
  }

  public char getGenero() {
    return genero;
  }

}
